// Generate star rating HTML
function generateStars(rating) {
    let stars = '';
    for (let i = 1; i <= 5; i++) {
        if (i <= Math.floor(rating)) {
            stars += '<span class="star filled">★</span>';
        } else if (i === Math.ceil(rating) && rating % 1 !== 0) {
            stars += '<span class="star half">★</span>';
        } else {
            stars += '<span class="star">★</span>';
        }
    }
    return stars;
}

// Render product cards into a container element
function renderProducts(container, products) {
    if (!container) return;
    if (!products || products.length === 0) {
        container.innerHTML = `
            <div class="no-products">
                <div class="no-products-icon">🔍</div>
                <h3>No products found</h3>
                <p>Try adjusting your search or filters.</p>
            </div>`;
        return;
    }

    container.innerHTML = products.map(p => `
        <div class="pro" data-product-id="${p.id}" data-category="${p.category}" data-price="${p.price}"
             onclick="window.location.href='product-detail.html?id=${p.id}'"
             style="cursor:pointer;">
            ${p.badge ? `<span class="pro-badge badge-${p.badge.toLowerCase()}">${p.badge}</span>` : ''}
            <img src="${p.image}" alt="${p.name}" loading="lazy">
            <span>${p.brand}</span>
            <h4>${p.name}</h4>
            <div class="pro-rating">
                ${generateStars(p.rating)}
                <small>(${p.reviews})</small>
            </div>
            <h3>₹${p.price.toLocaleString()}</h3>
            <div class="pro-actions" onclick="event.stopPropagation()">
                <button class="add-to-cart" onclick="addProductToCart(${p.id}, this)">
                    <i class="fas fa-shopping-cart"></i> Add to Cart
                </button>
                <a href="product-detail.html?id=${p.id}" class="view-detail-btn" title="View Details">
                    <i class="fas fa-eye"></i>
                </a>
            </div>
        </div>
    `).join('');
}
